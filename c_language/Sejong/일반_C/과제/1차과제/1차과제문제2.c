// 문제 2
#include <stdio.h>

int main(){
    int year;   // 입력받은 년도 변수 설정
    char a;     // 연도의 윤년인지 아닌지를 판단한 후 L,C를 저장할 변수 설정
    scanf("%d", &year);     // 연도 입력
    // 조건 연산자를 이용해 입력받은 연도가 4로 나누어 떨어지고 100으로 안나누어떨어지거나 400으로 나누어 떨어질 경우 변수a 에 L을 저장
    // 위 조건 이외에는 C를 저장
    a = (year % 4 == 0 && year % 100 != 0) || year % 400 == 0 ? 'L' : 'C';
    printf("%c", a); // a에 저장한 L또는 C를 출력
    return 0;
}